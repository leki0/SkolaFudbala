    private void jButtonUnesiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUnesiActionPerformed
        try {
            String url = jTextFieldUrl.getText();
            String username = jTextFieldUsername.getText();
            String password = String.valueOf(jPasswordFieldSifra.getPassword());
            String port = jTextFieldPort.getText();
            properties.put(ServerKonstante.DB_URL, url);
            properties.store(new FileOutputStream(ServerKonstante.PROPERTY_PATH), null);
            properties.put(ServerKonstante.DB_USERNAME, username);
            properties.store(new FileOutputStream(ServerKonstante.PROPERTY_PATH), null);
            properties.put(ServerKonstante.DB_PASSWORD, password);
            properties.store(new FileOutputStream(ServerKonstante.PROPERTY_PATH), null);
            properties.put(ServerKonstante.DB_PORT, port);
            properties.store(new FileOutputStream(ServerKonstante.PROPERTY_PATH), null);
            JOptionPane.showMessageDialog(this, "Sistem je sacuvao promjene!", "USPJESNO PROMJENJENO", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } catch (FileNotFoundException e) {
            System.out.println("Fajl properties nije pronadjen, GRESKA PRI UPISU U FAJL!");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne moze da sacuva promjene!", "GRESKA", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonUnesiActionPerformed


