    private void napuniFormu() {
        try {
            properties = new Properties();
            properties.load(new FileInputStream(ServerKonstante.PROPERTY_PATH));
            jTextFieldUrl.setText(properties.getProperty("url"));
            jTextFieldUsername.setText(properties.getProperty("username"));
            jPasswordFieldSifra.setText(properties.getProperty("password"));
            jTextFieldPort.setText(properties.getProperty("port"));
        } catch (FileNotFoundException ex) {
            System.out.println("Properties fajl nije pronadjen, GRESKA U CITANJU IZ FAJLA!");
        } catch (IOException ex) {
            System.out.println("Properties fajl nije pronadjen, GRESKA U CITANJU IZ FAJLA!");
        }
    }
