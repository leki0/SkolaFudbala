    public void popuniTabeluFudbaler() throws Exception {

        modelTabeleFudbaler = new ModelTabeleFudbalerTG();
        jTableFudbaleri.setModel(modelTabeleFudbaler);
    }

    private String validiraj(String ime, String jmbg) {
        String validan = "";
        if ((!ime.equals(""))) {

            if (!ime.matches("[a-zA-Z]+")) {
                validan += "Ime | ";
            }
        }
        if ((!jmbg.equals(""))) {

            if (!jmbg.matches("\\d+")) {
                validan += "JMBG | ";
            }
        }

        return validan;
    }

    private void osvjeziTabeluPretraga() {
        modelTabeleFudbaler = new ModelTabeleFudbalerTG(pretrazeniFudbaleri);
        jTableFudbaleri.setModel(modelTabeleFudbaler);
    }

    private void popuniComboTreningGrupa() throws Exception {
        listaTreningGrupa = Klijent.getInstance().ucitajListuTreningGrupe();
        for (TreningGrupa tg : listaTreningGrupa) {
            jComboBoxTreningGrupa.addItem(tg);
        }
        jComboBoxTreningGrupa.setSelectedIndex(-1);

    }

    private void osvjeziPolja() {
        jTextFieldIme.setText("");
        jTextFieldJmbg.setText("");
        jComboBoxTreningGrupa.setSelectedIndex(-1);
    }

    private boolean validirajPrazno(String ime, String jmbg, TreningGrupa tg) {
        if (ime.equals("") && jmbg.equals("") && tg.getObjID() == -1) {
            return true;
        }
        return false;
    }
