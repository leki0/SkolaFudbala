    private void jButtonPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPretraziActionPerformed
        try {
            String ime = jTextFieldIme.getText();
            String jmbg = jTextFieldJmbg.getText();
            TreningGrupa tg = (TreningGrupa) jComboBoxTreningGrupa.getSelectedItem();
            if (tg == null) {
                tg = new TreningGrupa(-1, null, -1, new Trener());
            }
            boolean prazno = validirajPrazno(ime, jmbg, tg);
            String validan = validiraj(ime, jmbg);
            if (!prazno) {
                if (validan.equals("")) {
                    Fudbaler fudbalerPretraga = new Fudbaler(ime, null, jmbg, null, tg, null);
                    pretrazeniFudbaleri = Klijent.getInstance().nadjiFudbalere(fudbalerPretraga);
                    if (pretrazeniFudbaleri.size() == 1) {
                        JOptionPane.showMessageDialog(this, "Sistem je pronašao fudbalera po zadatim vrijednostima!", "Pretraga", JOptionPane.INFORMATION_MESSAGE);
                        osvjeziPolja();
                    } else {
                        JOptionPane.showMessageDialog(this, "Sistem je pronašao fudbalere po zadatim vrijednostima!", "Pretraga", JOptionPane.INFORMATION_MESSAGE);
                    }
                    osvjeziTabeluPretraga();

                } else {
                    JOptionPane.showMessageDialog(this, "Neispravan format: " + validan, "GRESKA", JOptionPane.ERROR_MESSAGE);
                    jComboBoxTreningGrupa.setSelectedIndex(-1);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Morate da unesete bar jedno polje da biste izvršili pretragu!", "GRESKA", JOptionPane.ERROR_MESSAGE);

            }

        } catch (Exception ex) {
            if (ex instanceof IOException) {
                JOptionPane.showMessageDialog(this, "Greska! Pokusajte kasnije!", "GREŠKA", JOptionPane.ERROR_MESSAGE);
                System.exit(0);
            } else {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "GRESKA", JOptionPane.ERROR_MESSAGE);
                jComboBoxTreningGrupa.setSelectedIndex(-1);
                if (pretrazeniFudbaleri.size() > 0) {
                    modelTabeleFudbaler = new ModelTabeleFudbalerTG(pretrazeniFudbaleri);
                    jTableFudbaleri.setModel(modelTabeleFudbaler);
                }
                try {
                    modelTabeleFudbaler = new ModelTabeleFudbalerTG();
                } catch (Exception ex1) {
                    if (ex instanceof IOException) {
                        JOptionPane.showMessageDialog(this, "Greska! Pokusajte kasnije!", "GREŠKA", JOptionPane.ERROR_MESSAGE);
                        System.exit(0);
                    }
                }

                jTableFudbaleri.setModel(modelTabeleFudbaler);
                osvjeziPolja();
            }

        }
    }//GEN-LAST:event_jButtonPretraziActionPerformed

    private void jButtonIzmjeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIzmjeniActionPerformed
        if (jTableFudbaleri.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Sistem ne može da učita fudbalera! Morate selektovati bar jednog fudbalera!", "GRESKA", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                Fudbaler fudbaler = modelTabeleFudbaler.get(jTableFudbaleri.getSelectedRow());
                Fudbaler fudbalerBaza = Klijent.getInstance().ucitajFudbalera(fudbaler);

                JOptionPane.showMessageDialog(this, "Sistem je učitao fudbalera!", "Fudbaler učitan", JOptionPane.INFORMATION_MESSAGE);
                UnosIzmjenaFudbaleraForma utf = new UnosIzmjenaFudbaleraForma(this, Prikaz.IZMJENI_FORMA, fudbalerBaza);
                utf.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
                utf.setVisible(true);
            } catch (Exception ex) {
                if (ex instanceof IOException) {
                    JOptionPane.showMessageDialog(this, "Greska! Pokusajte kasnije!", "GREŠKA", JOptionPane.ERROR_MESSAGE);
                    System.exit(0);
                } else {
                    JOptionPane.showMessageDialog(this, ex.getMessage(), "GRESKA", JOptionPane.ERROR_MESSAGE);
                }
            }

        }
    }//GEN-LAST:event_jButtonIzmjeniActionPerformed

